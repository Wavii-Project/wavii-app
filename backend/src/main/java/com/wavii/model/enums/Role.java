package com.wavii.model.enums;

/**
 * Enum que define los roles de usuario en la plataforma.
 */
public enum Role {
    /** Usuario estándar (alumno) */
    USUARIO,
    /** Profesor que ofrece clases particulares */
    PROFESOR_PARTICULAR,
    /** Profesor certificado por Wavii */
    PROFESOR_CERTIFICADO,
    /** Administrador del sistema */
    ADMIN
}
